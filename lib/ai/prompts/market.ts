export interface MarketCompanyContext {
  name: string
  niche: string | null
  products: string[]
  regions: string[]
  competitors: string[]
  avgCheck: number | null
  margin: number | null
}

export function buildMarketAnalysisInput(company: MarketCompanyContext): string {
  return `ДАННЫЕ КОМПАНИИ:
Компания: ${company.name}
Ниша: ${company.niche ?? "не указана"}
Продукты: ${company.products.length ? company.products.join(", ") : "не указаны"}
Регионы: ${company.regions.length ? company.regions.join(", ") : "не указаны"}
Средний чек: ${company.avgCheck ? formatRub(company.avgCheck) : "не указан"}
Маржа: ${company.margin ? `${company.margin}%` : "не указана"}
Конкуренты: ${company.competitors.length ? company.competitors.join(", ") : "не указаны"}

Проведи анализ рынка по шагам из системного промта и заполни все поля схемы.`
}
