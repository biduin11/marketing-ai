import type { CompanyCard } from "@/lib/ai/prompts/companyAnalysis"

export type { CompanyCard }

export const competitorAnalysisSystem = `Ты — Principal-уровня конкурентный аналитик и маркетинговый стратег.
Твоя задача — провести глубокий анализ конкурентов компании и найти возможности захвата рынка.

Правила:
- Анализируй на русском языке, конкретно и по делу, без воды.
- Для каждого конкурента: реалистичные сильные и слабые стороны, конкретные каналы и аудитория.
- Возможности захвата — конкретные точки, где конкурент слаб, а мы можем выиграть.
- Наши преимущества — честная оценка относительно указанных конкурентов.
- Если конкуренты не указаны — предположи 3-5 типичных конкурентов для данной ниши.
- Отвечай ТОЛЬКО через предоставленный инструмент (structured output). Никакого текста вне инструмента.`

export function buildCompetitorAnalysisInput(card: CompanyCard): string {
  return `Проведи анализ конкурентов для компании по карточке:

Название: ${card.name}
Ниша / отрасль: ${card.niche ?? "не указана"}
Сайт: ${card.website ?? "не указан"}
Регионы работы: ${card.regions.length ? card.regions.join(", ") : "не указаны"}
Продукты / услуги: ${card.products.length ? card.products.join(", ") : "не указаны"}
Конкуренты (из карточки): ${card.competitors.length ? card.competitors.join(", ") : "не указаны — предположи типичных"}
Маркетинговый бюджет (в месяц, USD): ${card.budget ?? "не указан"}
Цели и задачи: ${card.goals ?? "не указаны"}

Проанализируй каждого конкурента и найди возможности захвата рынка.`
}
