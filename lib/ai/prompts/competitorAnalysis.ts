import type { CompanyCard } from "@/lib/ai/prompts/companyAnalysis"
import { formatRub } from "@/lib/utils"

export type { CompanyCard }

export const competitorAnalysisSystem = `Ты — Principal-уровня конкурентный аналитик и маркетинговый стратег.
Твоя задача — провести РЕАЛЬНЫЙ анализ конкурентов на основе живых данных из интернета.

ШАГ 1. СБОР ДАННЫХ (используй web_search для каждого конкурента):

Для каждого конкурента из списка выполни следующие поиски:

а) РЕПУТАЦИЯ: "[название конкурента] [город/регион] отзывы"
   → ищи рейтинг на Яндекс Картах (★), количество отзывов, частые жалобы и похвалы

б) ПОИСКОВАЯ ПОЗИЦИЯ: "[основной запрос ниши] [город]" (например "кровельные работы Махачкала")
   → определи позицию конкурента в органической выдаче (1–10)
   → есть ли объявление Яндекс Директ над результатами?

в) ОНЛАЙН-ПРИСУТСТВИЕ: "[название конкурента] сайт"
   → есть ли сайт? URL? Есть ли цены, калькулятор, форма онлайн-заявки?

г) СОЦСЕТИ: "[название конкурента] Instagram ВКонтакте Telegram"
   → найди аккаунты, оцени активность (последние посты — когда были?)

ШАГ 2. АНАЛИЗ:
- Сравни рейтинги: где конкурент слаб по отзывам? Это наша точка входа.
- Частые жалобы клиентов = наши ВОЗМОЖНОСТИ для захвата рынка.
- Позиция в поиске + реклама = насколько активно он борется за трафик.
- Онлайн-сервисы (цены, форма) = барьеры для перехода клиента.

ШАГ 3. ВЫВОД:
Вызови инструмент save_competitor_analysis со структурированным анализом.

Правила:
- Анализируй на русском языке, конкретно и честно.
- Если данные не найдены — не придумывай, пропускай поле (undefined).
- Жалобы клиентов — это точки роста для нас, выдели их явно в commonComplaints.
- Возможности с priority "high" — только те, где конкурент ЯВНО слаб по реальным данным.
- Наши слабости (ourWeaknesses) — честная оценка, где конкуренты нас превосходят.
- Отвечай ТОЛЬКО через предоставленный инструмент. Никакого текста вне инструмента.`

export function buildCompetitorAnalysisInput(card: CompanyCard): string {
  const city = card.regions.length ? card.regions[0] : "город не указан"
  const mainProduct = card.products.length ? card.products[0] : card.niche ?? "услуга"

  return `Проведи анализ конкурентов для компании.

НАША КОМПАНИЯ:
Название: ${card.name}
Ниша / отрасль: ${card.niche ?? "не указана"}
Основной продукт/услуга: ${card.products.length ? card.products.join(", ") : "не указаны"}
Регионы работы: ${card.regions.length ? card.regions.join(", ") : "не указаны"}
Сайт: ${card.website ?? "нет"}
Бюджет: ${formatRub(card.budget)} в месяц
Цели и задачи: ${card.goals ?? "не указаны"}

КОНКУРЕНТЫ ДЛЯ АНАЛИЗА:
${card.competitors.length ? card.competitors.map((c, i) => `${i + 1}. ${c}`).join("\n") : "Не указаны — определи 3-5 типичных конкурентов для данной ниши в регионе."}

КЛЮЧЕВЫЕ ПОИСКОВЫЕ ЗАПРОСЫ ДЛЯ ПОИСКА:
- Основной запрос: "${mainProduct} ${city}"
- Репутация: "[конкурент] ${city} отзывы"
- Сравнение: "${mainProduct} ${city} лучшие компании"

Проведи поиск по каждому конкуренту и заполни все доступные поля анализа.`
}
