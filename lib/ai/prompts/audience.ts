function formatCard(card: CompanyCard): string {
  return `Название: ${card.name}
Ниша / отрасль: ${card.niche ?? "не указана"}
Сайт: ${card.website ?? "не указан"}
Регионы работы: ${card.regions.length ? card.regions.join(", ") : "не указаны"}
Продукты / услуги: ${card.products.length ? card.products.join(", ") : "не указаны"}
Конкуренты: ${card.competitors.length ? card.competitors.join(", ") : "не указаны"}
Маркетинговый бюджет (в месяц, USD): ${card.budget ?? "не указан"}
Цели и задачи: ${card.goals ?? "не указаны"}`
}

export function buildAudienceSegmentsInput(card: CompanyCard): string {
  return `Выдели ключевые сегменты целевой аудитории для компании по карточке:

${formatCard(card)}`
}

export function buildBuyerPersonaInput(card: CompanyCard): string {
  return `Создай детальные Buyer Persona для компании по карточке:

${formatCard(card)}`
}

export function buildJtbdInput(card: CompanyCard): string {
  return `Выяви ключевые Jobs To Be Done для компании по карточке:

${formatCard(card)}`
}
