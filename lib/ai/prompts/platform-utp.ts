export const platformUtpSystem = `Ты — эксперт по маркетингу и копирайтингу.

${psychotypesBlock}

ЗАДАЧА:
По площадке и данным компании создай УТП сразу для всех 4 психотипов покупателей.
Учитывай специфику площадки и психотип одновременно — заголовок, текст и советы
должны звучать так, как реально пишут именно на этой площадке, а не как общий текст.

Правила:
- Пиши на русском языке, конкретно, без воды.
- headline — до 10 слов, subheadline — одно предложение.
- fullText — готовый к публикации текст объявления с учётом формата площадки
  (длина, структура, допустимые элементы вроде хэштегов/эмодзи — только если это
  уместно на площадке).
- tips — 2-4 практических совета по оформлению именно для этой площадки.
- Отвечай ТОЛЬКО через предоставленный инструмент (structured output). Никакого текста вне инструмента.`

export function buildPlatformUtpInput(card: CompanyCard, platform: PlatformKey): string {
  const meta = PLATFORM_META[platform]
  return `ДАННЫЕ КОМПАНИИ:
Название: ${card.name}
Ниша / отрасль: ${card.niche ?? "не указана"}
Продукты / услуги: ${card.products.length ? card.products.join(", ") : "не указаны"}
Регионы работы: ${card.regions.length ? card.regions.join(", ") : "не указаны"}
Конкуренты: ${card.competitors.length ? card.competitors.join(", ") : "не указаны"}

ПЛОЩАДКА: ${meta.label}

ОСОБЕННОСТИ ПЛОЩАДКИ:
${meta.playbook}

Создай УТП для площадки «${meta.label}» сразу под все 4 психотипа покупателей,
с учётом специфики площадки выше.`
}
