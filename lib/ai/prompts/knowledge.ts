export const contentStrategyBlock = `ПРАВИЛА КОНТЕНТ-СТРАТЕГИИ (обязательно применяй):

СПЛИТ 70/20/10:
70% — образовательный/полезный (строит доверие и экспертность),
20% — вовлекающий (истории, кейсы, за кулисами, опросы),
10% — продающий (офферы, акции, призывы).
Никогда не делай план, где продающего больше 15%.

КЛИЕНТ — ГЕРОЙ КОНТЕНТА (StoryBrand):
Формула каждого поста: Боль клиента → Решение → Результат (с доказательством).
Избегай «мы лучшие» — только «вот как мы решили вашу проблему».

ЯЗЫК КЛИЕНТА:
Если в анкете заполнено поле «Как клиенты описывают проблему своими словами» —
используй ДОСЛОВНО эти фразы в темах постов и заголовках.

СТРУКТУРА REELS-СЦЕНАРИЯ:
- Хук (первые 3 секунды): конкретный вопрос или факт, ломающий ожидание.
- Проблема: боль клиента своими словами.
- Решение: конкретный шаг/продукт/подход.
- Доказательство: цифра, кейс, отзыв.
- CTA: одно конкретное действие.

ТЕМЫ ПО ПСИХОТИПАМ:
Традиционалист: «Как мы делаем X уже 10 лет», «Отзыв клиента», «Гарантия».
Независимый: «Технический разбор», «Чем мы отличаемся», «Честно о рынке».
Эстет: «История создания», «Детали которые важны», «Процесс работы».
Гедонист: «Вау-момент», «До/после», «Реакция клиента», «Челлендж».

BRAND VS MARKETING БАЛАНС В КОНТЕНТ-ПЛАНЕ:
- Brand-контент: строит идентичность и доверие (долгосрочно).
- Marketing-контент: генерирует заявки и охват (краткосрочно).
Если весь план только продажи или только «мы крутые» — это слабая стратегия.`

