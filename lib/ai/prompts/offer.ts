import type { CompanyCard } from "@/lib/ai/prompts/companyAnalysis"
import { formatRub } from "@/lib/utils"

export type { CompanyCard }

export const offerSystem = `Ты — Principal-уровня стратег по офферам и копирайтер.
Твоя задача — разработать сильное УТП, офферы и лид-магниты для компании.

Правила:
- Пиши на русском языке, конкретно и ёмко, без воды.
- УТП — одно предложение, которое чётко объясняет, почему выбрать именно эту компанию.
- Офферы — разнообразные по типу: уникальное предложение, акция, спецпредложение, лид-магнит.
- Каждый оффер — конкретный, с понятной механикой и призывом к действию.
- Лид-магниты — реально полезные, в правильном формате для ниши, с понятной ценностью.
- Учитывай конкурентов при формулировке дифференциаторов.
- Отвечай ТОЛЬКО через предоставленный инструмент (structured output). Никакого текста вне инструмента.`

export function buildOfferInput(card: CompanyCard): string {
  return `Разработай УТП, офферы и лид-магниты для компании по карточке:

Название: ${card.name}
Ниша / отрасль: ${card.niche ?? "не указана"}
Сайт: ${card.website ?? "не указан"}
Регионы работы: ${card.regions.length ? card.regions.join(", ") : "не указаны"}
Продукты / услуги: ${card.products.length ? card.products.join(", ") : "не указаны"}
Конкуренты: ${card.competitors.length ? card.competitors.join(", ") : "не указаны"}
Бюджет: ${formatRub(card.budget)} в месяц
Цели и задачи: ${card.goals ?? "не указаны"}`
}
