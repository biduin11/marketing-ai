export interface ProductDetailContext {
  name: string
  margin: number | null
  salesShare: number | null
  stage: string | null
}

export interface ProductCompanyContext {
  name: string
  niche: string | null
  products: string[]
  regions: string[]
  avgCheck: number | null
  margin: number | null
  productsDetailed: ProductDetailContext[]
}

export function buildProductAnalysisInput(company: ProductCompanyContext): string {
  const productsContext = company.productsDetailed.length
    ? company.productsDetailed
        .map(
          (p) =>
            `${p.name}: маржа ${p.margin ?? "?"}%, доля продаж ${p.salesShare ?? "?"}%, стадия: ${p.stage ?? "?"}`
        )
        .join("\n")
    : "Детальные данные не указаны"

  return `ДАННЫЕ КОМПАНИИ:
Компания: ${company.name}
Ниша: ${company.niche ?? "не указана"}
Продукты: ${company.products.length ? company.products.join(", ") : "не указаны"}
Регионы: ${company.regions.length ? company.regions.join(", ") : "не указаны"}
Средний чек: ${company.avgCheck ? formatRub(company.avgCheck) : "не указан"}
Маржа компании: ${company.margin ? `${company.margin}%` : "не указана"}

ДАННЫЕ О ПРОДУКТАХ (если указаны клиентом):
${productsContext}

Проведи анализ по шагам из системного промта и заполни все поля схемы.`
}
